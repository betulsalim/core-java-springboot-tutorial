public class Main {
    public static void main(String[] args) {

        int myInt = 10;
        System.out.println("My int: " + myInt);

        //32 bit or 4 bytes
        float myFloat = 10;
        System.out.println("My Float: " + myFloat);

        //64 bit or 8 bytes
        double myDouble = 10;
        System.out.println("My Double: " + myDouble);

        myFloat = 10.25f;
        System.out.println("My new float: " + myFloat);

        myDouble = 10.333;
        System.out.println("My new Double: " + myDouble);

        myFloat = 10.25f / 2f;
        System.out.println("My float which divided by 2: " + myFloat);

        myDouble = 10.333 / 2;
        System.out.println("My Double which divided by 2: " + myDouble);

    }
}