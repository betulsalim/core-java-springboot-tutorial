public class Main {
    public static void main(String[] args) {
        //1 bit
        boolean myBoolean = true;
        System.out.println("My Boolean : " + myBoolean);

        //32 bit or 8 bytes
        char myChar = 'A';
        System.out.println("My Char: " + myChar);

        myChar = '\u0043';
        System.out.println("My new Char: " + myChar);
    }
}